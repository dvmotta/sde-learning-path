age = 23 # integer type variable

# addition operator
sum_age = age + 7 # adding 7 to the age variable
print(f"In 7 years, I will be {sum_age} years old.")

# subtraction operator
y_until_30 = 30 - age # calculating how many years until I turn 30
print(f"I will turn 30 in {y_until_30} years.")

# multiplication operator
double_age = age * 2 # multiplying the age by 2
print(f"My age doubled is {double_age}.")

# division operator
half_age = age / 2 # dividing the age by 2
print(f"My age halved is {half_age}.")

# modulus operator
remainder = age % 2 # calculating the remainder of age divided by 2
print(f"The remainder when dividing my age by 2 is {remainder}.")

# integer division operator
integer_division = age // 2 # performing integer division of age by 2
print(f"The result of integer division of my age by 2 is {integer_division}.")

# exponentiation operator
age_squared = age ** 2 # raising the age to the power of 2
print(f"My age squared is {age_squared}.")

