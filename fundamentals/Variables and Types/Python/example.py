name = "Davi" # string type variable
age = 23 # integer type variable
height = 1.75 # float type variable
student = True # boolean type variable, it can be either True or False

shopping_list = ["milk", "bread", "eggs"] # list type variable
person_dict = {"name": "Davi", "age": 23, "height": 1.75} # dictionary type variable
color_tuple = ("red", "green", "blue") # tuple type variable
set_numbers = {1, 2, 3, 4, 5} # set type variable

# printing the simple variables to the console
print(f"Hello, my name is {name}, I am {age} years old and {height}m tall. This is our example with simple variables!") # f-string to print the variables in a formatted way

# printing the complex variables to the console
print(f"Our shopping list is: {shopping_list}")
print(f"An example with a dictionary with the information we have: {person_dict}")
print(f"A tuple of colors to exemplify its usage: {color_tuple}")
print(f"An example of a set of numbers: {set_numbers}")